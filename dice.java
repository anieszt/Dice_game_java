public class dice {
    
    private int Nface;
    private int RES;

    public dice(int Nface , int RES) {
        
        this.Nface=Nface;
        this.RES=RES;
        
    }

    public int getNface() {
        return Nface;
    }

    public void setNface(int Nface) {
        this.Nface = Nface;
    }

    public int getRES() {
        return RES;
    }

    public void setRES(int RES) {
        this.RES = RES;
    }
    
       
}
